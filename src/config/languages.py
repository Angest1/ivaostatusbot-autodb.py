def get_text(lang_code: str, key: str, **kwargs) -> str:
    """
    Get translated text for key and language code.
    Falls back to 'en' if language or key not found.
    Allows string formatting via kwargs.
    """
    # Normalize language code (e.g. pt-BR -> pt)
    lang = lang_code.split('-')[0].lower()
    
    # Get dictionary for language, fallback to en
    resources = LANGUAGES.get(lang, LANGUAGES.get("en", {}))
    
    # Get string, fallback to en if key missing in target language
    text = resources.get(key)
    if text is None:
        text = LANGUAGES["en"].get(key, key)
    
    # Format if arguments provided
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
            
    if lang == 'ar':
        text = '\u202A' + text + '\u202C'
        
    return text
